function interviewPop() {
  var interviewBt = $('#interviewBt'),
    interviewList = $('.person_list'),
    interviewListItem = $('.person_list li a'),
    interviewPopup = $('.item'),
    closeBtn = $('.close_btn'),
    bg = $('#pop_mask_id');

  interviewBt.on('click', function (e) {
    e.preventDefault();
    if (interviewList.hasClass('show')) {
      interviewList.removeClass('show');
    } else {
      interviewList.addClass('show');
    }
  })

  interviewListItem.on('click', function (e) {
    e.preventDefault();
    var thisItem = $(this).next();
    if (thisItem.hasClass('show')) {
      thisItem.removeClass('show');
      bg.removeClass('show');
    } else {
      thisItem.addClass('show');
      bg.addClass('show');
      if (winWidth > 959) {
        //PC
        //PC에서 인터뷰팝업 리스트에서 사람이름 클릭시 fullpage 스크롤 작동X
        $.fn.fullpage.setAllowScrolling(false);
        $.fn.fullpage.setKeyboardScrolling(false);
      } else {
        //M
        $('body').addClass('hidden').on('touchmove', function (e) {
          e.preventDefault();
        });
      }
    }
  })

  bg.on('click', function () {
    interviewPopup.removeClass('show');
    interviewList.removeClass('show')
    bg.removeClass('show');
    $('.interview_item').removeClass('show')

    //bg 눌렀을때 다시 fullpage 작동 
    $.fn.fullpage.setAllowScrolling(true);
    $.fn.fullpage.setKeyboardScrolling(true);
  })

  closeBtn.on('click', function (e) {
    e.preventDefault();
    interviewPopup.removeClass('show');
    interviewList.removeClass('show')
    bg.removeClass('show');

    if (winWidth > 959) {
      //PC
      //PC에서 인터뷰팝업창 닫기 버튼 클릭시 fullpage 스크롤 작동O
      $.fn.fullpage.setAllowScrolling(true);
      $.fn.fullpage.setKeyboardScrolling(true);
    } else {
      //M
      $('body').removeClass('hidden').off('touchmove');
    }
  })
} //interview popup


function headerEvent() {
  var headerWrap = $('header'),
    // commonGnb = $('.gnb_b_v'),
    commonGnb = $('#nav2020'),
    mainGnb = $('.header_top'),
    nav = $('.header_bottom'),
    intro = $('.intro');

  var lastScrollTop = 0,
    delta = 10; //동작 구현 시작 위치 

  if (winWidth > 959) {
    //PC
    $(window).on('mousewheel', function (e) {
      var wheel = e.originalEvent.wheelDelta;
      if ($('.item').hasClass('show')) {
        headerWrap.slideDown(500);
      } else if ((wheel > 0) && (intro.hasClass('active'))) {
        headerWrap.slideDown(450);
      } else {
        headerWrap.slideUp(450);
      }
    });
  } else {
    //M
    $(window).scroll(function (e) {
      var st = $(this).scrollTop(); //현재 스크롤 저장 
      if (Math.abs(lastScrollTop - st) <= delta) return; // 설정한 delta 값보다 더 스크롤되었는지 확인 
      if ((st > lastScrollTop) && (lastScrollTop > 0)) {
        //scroll down
        commonGnb.slideUp(430)
        mainGnb.slideUp(430)
        nav.slideUp(430)
      } else {
        //scroll up
        if (st < 200) {
          //스크롤 up할 경우, 최상단 부분에서 모든 header 노출 
          commonGnb.slideDown(430)
          mainGnb.slideDown(430)
          nav.slideDown(430)
        } else {
          //스크롤 up할 경우, 최상단전에는 nav만 노출
          commonGnb.slideUp(430)
          mainGnb.slideUp(430)
          nav.slideDown(400)
        }
      }
      lastScrollTop = st; //현재 위치 지정 
    });
  }
}

function sectionInterview() {
  var secInterviewBt = $('.section_interview'),
    secInterviewItem = $('.interview_item'),
    secInterviewClose = $('#sec_interview_close');

  secInterviewBt.on('click', function () {
    if (secInterviewItem.hasClass('show')) {
      secInterviewItem.removeCLass('show')
      $('#pop_mask_id').removeClass('show');
    } else {
      secInterviewItem.addClass('show');
      $('#pop_mask_id').addClass('show');
      if (winWidth > 959) {
        $.fn.fullpage.setAllowScrolling(false);
        $.fn.fullpage.setKeyboardScrolling(false);
      } else {
        $('body').addClass('hidden').on('touchmove', function (e) {
          e.preventDefault();
        });
      }
    }
  })

  secInterviewClose.on('click', function (e) {
    e.preventDefault();
    secInterviewItem.removeClass('show');

    if (winWidth > 959) {
      $.fn.fullpage.setAllowScrolling(true);
      $.fn.fullpage.setKeyboardScrolling(true);
    } else {
      $('body').removeClass('hidden').off('scroll touchmove mousewheel');
    }
  })
}
